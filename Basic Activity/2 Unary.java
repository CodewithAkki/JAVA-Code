public class Unary {
   
    public static void main(String args[]) {
        int x=10;
        System.out.println("preincrement:\t"+(++x));
        System.out.println("postincrement:\t"+(x++));
        System.out.println("predecrement:\t"+(--x));
        System.out.println("postdecrement:\t"+(x--));
        System.out.println("Current value:\t"+(x));
    }
}
